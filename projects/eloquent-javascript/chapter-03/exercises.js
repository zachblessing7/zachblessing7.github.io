////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if(num1 < num2){
    return num1;
  } else {
    return num2;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  num = Math.abs(num);
  if(num === 0){
    return true;
  } else if (num === 1){
    return false;
  } else {
    return isEven(num - 2);
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, character) {
  var count= 0;
  str.toLowerCase().split('').forEach(function(item){
   item===character.toLowerCase() ? count++:null;
  });
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
var count= 0;
  str.toLowerCase().split('').forEach(function(item){
   item==='b' ? count++:null;
  });
  return count; 
}

